package com.kh;

import java.util.Scanner;

public class N1072 {

	public static void main(String[] args) {

		//n 입력, n개의 숫자가 입력됨, n개의 숫자들을 출력
		
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		
		for(int i=0; i<n; ++i) {
			//입력
			int x = sc.nextInt();
			
			//출력
			System.out.println(x);
			
		}
		
	}

}
